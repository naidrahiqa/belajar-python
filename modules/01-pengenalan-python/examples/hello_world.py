# Mencetak teks ke layar menggunakan fungsi print()
print("Hello, World!")

# Kita juga bisa print beberapa baris
print("Selamat datang di Python!")
print("Mari kita belajar bersama")

# Menambahkan garis pemisah
print("-" * 40)  # Akan mencetak 40 karakter '-'

# Variabel dan print
nama = "Programmer Python"
print(f"Halo, {nama}!")

# Print dengan berbagai cara
print("Cara 1: Concatenation")
print("Nama saya " + nama)

print("\nCara 2: F-string (Python 3.6+)")
print(f"Nama saya {nama}")

print("\nCara 3: .format()")
print("Nama saya {}".format(nama))

print("\nCara 4: Multiple arguments")
print("Nama saya", nama)

